                                <p>Európska komisia v polovici svojho mandátu uverejnila priebežné hodnotenie stratégie digitálneho jednotného trhu. Jeho súčasťou je bilancia dosiahnutého pokroku, ako aj náčrt budúcich opatrení týkajúcich sa online platforiem, dátového hospodárstva a kybernetickej bezpečnosti. Od mája 2015 predložila Európska komisia 35 legislatívnych návrhov a politických iniciatív tak, ako bolo oznámené v stratégii pre digitálnyjednotný trh. </p>
                                
                                <p>Teraz sa treba zamerať na dosiahnutie politickej dohody s Európskym parlamentom a Radou pri každomz návrhov, ale hlavne v otázke aktualizácietelekomunikačných predpisov EÚ, vďaka ktorým sa zvýšia investície do vysokorýchlostných a kvalitných sietí ako rozhodujúcej súčasti ďalšieho zavádzania digitálnej ekonomiky a spoločnosti.</p>
                            
                                <p>V  oznámení EK zo dňa 10.5.2017, ktoré sa týka priebežného hodnotenia, identifikovala Komisia tri hlavné oblasti vyžadujúce si ďalšie kroky Únie:</p>
                            
                                <p>V oblasti dátového hospodárstva Komisia pripravuje legislatívnu iniciatívu týkajúcu sa cezhraničnej voľnej výmeny údajov, ktoré nemajú osobný charakter (jeseň 2017), ako aj iniciatívu v súvislosti s dostupnosťou a opakovaným používaním verejných alebo z verejných zdrojov financovaných údajov (jar 2018). V oblasti kybernetickej bezpečnosti Komisia do septembra 2017 preskúma stratégiu EÚ v oblasti kybernetickej bezpečnosti a mandát Agentúry Európskej únie pre sieťovú a informačnú bezpečnosť (ENISA), aby ho zosúladila s novým rámcom pre kybernetickú bezpečnosť v rámci EÚ. </p>
        
                                <p>Komisia bude aj naďalej aktívna pri navrhovaní dodatočných opatrení týkajúcich sa noriem kybernetickej bezpečnosti, certifikácie a označovania, aby zvýšila kybernetickú bezpečnosť prepojených objektov.</p>
        
                                <p>V oblasti online platforiem predstaví Komisia do konca roka 2017 iniciatívu na riešenie nespravodlivých zmluvných podmienok a obchodných praktík zistených vo vzťahoch typu platforma – podnik, pričom už nedávno v tomto smere prijala rozhodnutia týkajúce sa presadzovania pravidiel hospodárskej súťaže, ktoré s tým súvisia. Komisia iniciovala niekoľko dialógov so zástupcami online platforiem v rámci digitálneho jednotného trhu (napr. internetové fórum EÚ, kódex správania pri nezákonných prejavoch nenávisti na internete<br/> 
                                a memorandum o porozumenív otázkach predaja falšovaného tovaru prostredníctvom internetu) a plánuje ich lepšiu koordináciu. Jedným z cieľov je dosiahnuť pokrok v procesných aspektoch a zásadách týkajúcich sa odstraňovania nelegálneho obsahu – mechanizmus<br/> 
                                oznamovania a prijímania opatrení – založených na transparentnosti a ochrane základných práv.</p>
                                
